# string
msg: str = "Hello world, this is a variable"
print(msg)
print(type(msg))

# integers
number: int = 90
print(number)
print(type(number))

# floats
number_with_decimal: float = 99.9
print(number_with_decimal)
print(type(number_with_decimal))

# booleans
is_verified: bool = False
print(is_verified)
print(type(is_verified))
